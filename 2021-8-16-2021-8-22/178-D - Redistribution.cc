int solve(void)
{
	if (n <= 2) return 0;
	const int mod = (int)1e9 + 7;
	vector<int> f(n + 1, 0);
	f[3] = 1;
	for (int k = 3; k <= n; ++k) {
		if (k != 3) f[k] = (f[k - 1] + f[k]) % mod;
		if (k + 3 <= n) f[k + 3] = (f[k + 3] + f[k]) % mod;
	}
	return f[n];
}

